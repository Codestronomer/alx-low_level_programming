#include "ops.h"

/**
 * add - adds two integers
 * @a: integer
 * @b: integer
 *
 * Return: result of operation
 */
int add(int a, int b)
{
	return (a + b);
}


/**
 * sub - subtracts two integers
 * @a: integer
 * @b: integer
 *
 * Return: result of operation
 */
int sub(int a, int b)
{
	return (a - b);
}


/**
 * mul - multiplies two integers
 * @a: integer
 * @b: integer
 *
 * Return: result of operation
 */
int mul(int a, int b)
{
	return (a * b);
}


/**
 * div - division of two integers
 * @a: integer
 * @b: integer
 *
 * Return: result of operation
 */
int div(int a, int b)
{
	return (a / b);
}


/**
 * mod - modulos of two integers
 * @a: integer
 * @b: integer
 *
 * Return: result of operation
 */
int mod(int a, int b)
{
	return (a % b);
}
